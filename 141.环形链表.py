# @lc code=start
# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def hasCycle(self, head):
        """
        :type head: ListNode
        :rtype: bool
        """
        if not head: return False
        visited = set()
        while head:
            if id(head) in visited:
                return True
            visited.add(id(head))
            head = head.next
        return False

